// Generates src/generated/licenses.json: the license attribution for every
// production (bundled) npm dependency, resolved transitively from the installed
// node_modules tree. Run automatically before `dev`/`build` (see package.json),
// or on demand with `npm run licenses`.
//
// Pure Node built-ins, no extra dependency. Output is sorted and timestamp-free
// so it only changes when the dependency set actually changes.

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const webRoot = resolve(scriptDir, "..");
const outFile = join(webRoot, "src", "generated", "licenses.json");

function readJson(path) {
  return JSON.parse(readFileSync(path, "utf8"));
}

// Packages that appear in the production dependency graph but are not part of the
// shipped bundle, so they need no attribution. Skipped entirely (not recorded and
// not traversed), which also prunes anything reachable only through them:
//   * @types/*       — type declarations (.d.ts), never emitted into the bundle.
//   * *babel-plugin* — build-time Babel plugins (e.g. @emotion/babel-plugin),
//                      mislabeled by some packages as runtime dependencies. This
//                      prunes the @babel/* compiler subtree while leaving the
//                      genuinely-shipped @babel/runtime (reached via other deps).
function isShipped(name) {
  if (name.startsWith("@types/")) return false;
  if (name.includes("babel-plugin")) return false;
  return true;
}

// Resolve a package directory the way Node does: walk up from the requiring
// package looking for node_modules/<name>, falling back to the top-level
// node_modules (npm hoists most packages there).
function findPackageDir(name, fromDir) {
  let dir = fromDir;
  for (;;) {
    const candidate = join(dir, "node_modules", name);
    if (existsSync(join(candidate, "package.json"))) return candidate;
    const parent = dirname(dir);
    if (parent === dir) break;
    dir = parent;
  }
  const top = join(webRoot, "node_modules", name);
  return existsSync(join(top, "package.json")) ? top : null;
}

function normalizeLicense(pkg) {
  if (typeof pkg.license === "string") return pkg.license;
  if (pkg.license && typeof pkg.license === "object" && pkg.license.type) return pkg.license.type;
  if (Array.isArray(pkg.licenses)) return pkg.licenses.map((l) => l.type || l).join(" OR ");
  return "UNKNOWN";
}

function normalizeRepoUrl(url) {
  return url
    .replace(/^git\+/, "")
    .replace(/^git:\/\//, "https://")
    .replace(/^github:/, "https://github.com/")
    .replace(/\.git$/, "");
}

function homepage(pkg) {
  if (pkg.homepage) return pkg.homepage;
  const repo = pkg.repository;
  if (typeof repo === "string") return normalizeRepoUrl(repo);
  if (repo && repo.url) return normalizeRepoUrl(repo.url);
  return `https://www.npmjs.com/package/${pkg.name}`;
}

const rootPkg = readJson(join(webRoot, "package.json"));
const seen = new Map();
const queue = Object.keys(rootPkg.dependencies || {}).map((name) => ({ name, from: webRoot }));

while (queue.length > 0) {
  const { name, from } = queue.shift();
  if (!isShipped(name)) continue;
  const dir = findPackageDir(name, from);
  if (!dir) continue;

  const pkg = readJson(join(dir, "package.json"));
  if (seen.has(pkg.name)) continue;

  seen.set(pkg.name, {
    name: pkg.name,
    version: pkg.version,
    license: normalizeLicense(pkg),
    url: homepage(pkg),
  });

  for (const dep of Object.keys(pkg.dependencies || {})) {
    queue.push({ name: dep, from: dir });
  }
}

const packages = [...seen.values()].sort((a, b) => a.name.localeCompare(b.name));

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(
  outFile,
  JSON.stringify(
    { _comment: "Generated by scripts/generate-licenses.mjs — do not edit by hand.", packages },
    null,
    2,
  ) + "\n",
);

console.log(`Wrote ${packages.length} dependency licenses to ${outFile.replace(webRoot + "/", "")}`);
